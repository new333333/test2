		//stub.folder_uploadFileAsByteArray(null, testEntryId, null, "下記.txt", "下記".getBytes("UTF-8"));
		stub.folder_uploadFileAsByteArray(null, testEntryId, null, text, "\u4e0b\u8a18".getBytes("UTF-8"));
