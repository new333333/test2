	private static String toString(Object[] objs) {
		if(objs == null)
			return null;
		StringBuilder sb = new StringBuilder();
		for(Object obj:objs) {
			if(sb.length() > 0)
				sb.append(", ");
			sb.append(obj.toString());
		}
		return sb.toString();
	}
	
